name = "Rahul"  # Rahul is a String Variable.
print(name, type(name))
age = 18  # 18 is a Integer.
print(age, type(age))
height = 5.10  # 5.10 is a Float value.
print("Your Height is:", height, "Type of Value=", type(height))
a, b, c, d = 10, 69.69, 'Python', True  # True or False is a Boolean Value.
print("a=", a, "Type=", type(a))
print("b=", b, "Type=", type(b))
print("c=", c, "Type=", type(c))
print("d=", d, "Type=", type(d))
# Mutable Objects are: lists, dict, etc.
# Immutable Objects are: int, str, float, etc.
